"""What is List? How will you reverse a list? """
"""
 list is a mutable, ordered collection of elements, where each element can be of any data type,
including integers, strings, floats, or even other lists.

characteristics : 

Ordered: Lists maintain the order of elements. The order in which elements are added to the list is preserved.

Mutable: Lists are mutable, meaning you can change, add, or remove elements after the list is created.

Dynamic: Lists in Python are dynamic, meaning they can grow or shrink in size as needed. You can add or remove elements 
from a list without specifying its size beforehand.


Indexed: Elements in a list are indexed starting from 0. This allows you to access individual elements using their index.

Iterable: Lists are iterable, meaning you can loop over the elements in a list using a for loop or other iteration constructs.


Flexible: Lists can be used in a wide range of scenarios, from simple storage of data to complex data structures and algorithms.

"""
my_list = [10,20,30,40,50]
my_list.reverse()
print(my_list)  
