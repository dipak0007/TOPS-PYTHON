"""How will you remove last object from a list? Suppose list1 is [2, 33, 222, 14, and 25], what is list1 [-1]? """

"""To remove the last object from a list in Python, you can use the pop() method 
The pop() method without any argument removes and returns the last element from the list. """

list1 = [2,33,222,14,25]
list1.pop()
print(list1)