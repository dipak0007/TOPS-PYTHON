"""In object-oriented programming (OOP) terminology, instantiation refers to the process of creating an 
instance (or object) of a class.

When you define a class in Python or any other object-oriented language, you're essentially creating a 
blueprint or a template that describes the attributes (data) and methods (functions) that the objects of 
that class will have. However, it's only when you create an instance of that class that you actually allocate
 memory for the object and initialize its attributes.

Instantiation involves calling the class name followed by parentheses, optionally passing any required arguments
 to the class's constructor (the __init__() method in Python). This process triggers the creation of a new object
   based on the class's blueprint."""